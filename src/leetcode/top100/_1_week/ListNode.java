package leetcode.top100._1_week;

/**
 * @ClassName ListNode
 * @Description AddTwoNumbersDemo 解题使用：单链表
 * @Author yk
 * @Date 2020/4/28 11:29
 * @Version 1.0
 **/
public class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
